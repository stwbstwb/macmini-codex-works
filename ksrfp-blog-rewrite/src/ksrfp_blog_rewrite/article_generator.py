def render_life_planning_article(
    title: str,
    brief: dict[str, Any],
    outline: list[dict[str, Any]],
    include_sources: bool,
) -> str:
    lines = [f"# {title}", ""]
    paragraphs_by_heading = life_planning_paragraphs(brief)

    for section in outline:
        h2 = str(section["h2"])
        lines.extend([f"## {h2}", ""])
        for paragraph in paragraphs_by_heading.get(h2, []):
            lines.extend([paragraph, ""])
        for h3 in section.get("h3", []):
            h3_text = str(h3)
            lines.extend([f"### {h3_text}", ""])
            for paragraph in paragraphs_by_heading.get(h3_text, []):
                lines.extend([paragraph, ""])

    if include_sources:
        lines.extend(render_reference_section(official_sources_for_topic("life_planning")))

    return "\n".join(lines).rstrip() + "\n"


