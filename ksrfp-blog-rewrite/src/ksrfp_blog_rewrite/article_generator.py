def generate_rewrite_article(brief: dict[str, Any], settings: dict[str, Any]) -> dict[str, Any]:
    generated_at = datetime.now().isoformat(timespec="seconds")
    generation_settings = settings.get("article_generation", {}) if isinstance(settings.get("article_generation"), dict) else {}
    topic_type = classify_topic(brief)
    title_candidates = build_title_candidates(brief, topic_type)
    title = title_candidates[0]
    outline = build_outline(brief, topic_type)
    body_markdown = render_article(title, brief, topic_type, outline, bool(generation_settings.get("include_reference_notes", True)))
    quality = measure_article_quality(body_markdown)

    return {
        "status": "ok",
        "generated_at": generated_at,
        "source_post_id": (brief.get("source") or {}).get("post_id") if isinstance(brief.get("source"), dict) else None,
        "topic_type": topic_type,
        "title": title,
        "title_candidates": title_candidates,
        "target_seo_keyword": (brief.get("extraction") or {}).get("target_seo_keyword")
        if isinstance(brief.get("extraction"), dict)
        else "",
        "outline": outline,
        "body_markdown": body_markdown,
        "body_text": markdown_to_plain_text(body_markdown),
        "quality": quality,
        "quality_gate": {
            "target_character_count": int(generation_settings.get("target_character_count") or 4500),
            "minimum_h2_count": int(generation_settings.get("minimum_h2_count") or 8),
            "minimum_h3_count": int(generation_settings.get("minimum_h3_count") or 12),
            "passed": quality["character_count"] >= int(generation_settings.get("target_character_count") or 4500)
            and quality["h2_count"] >= int(generation_settings.get("minimum_h2_count") or 8)
            and quality["h3_count"] >= int(generation_settings.get("minimum_h3_count") or 12),
        },
        "official_sources": official_sources_for_topic(topic_type),
    }


def classify_topic(brief: dict[str, Any]) -> str:
    text = " ".join(
        [
            str((brief.get("extraction") or {}).get("rewrite_theme") or "") if isinstance(brief.get("extraction"), dict) else "",
            str((brief.get("extraction") or {}).get("target_seo_keyword") or "") if isinstance(brief.get("extraction"), dict) else "",
            str((brief.get("extraction") or {}).get("target_reader") or "") if isinstance(brief.get("extraction"), dict) else "",
            str((brief.get("extraction") or {}).get("source_theme") or "") if isinstance(brief.get("extraction"), dict) else "",
            " ".join(str(value) for value in ((brief.get("extraction") or {}).get("related_keywords") or []))
            if isinstance(brief.get("extraction"), dict)
            else "",
            str((brief.get("source") or {}).get("title") or "") if isinstance(brief.get("source"), dict) else "",
            " ".join(str(value) for value in ((brief.get("source") or {}).get("category_names") or []))
            if isinstance(brief.get("source"), dict)
            else "",
            " ".join(str(value) for value in ((brief.get("source") or {}).get("tag_names") or []))
            if isinstance(brief.get("source"), dict)
            else "",
        ]
    )
    if is_life_planning_topic(text):
        return "life_planning"
    if "懲戒" in text and "就業規則" in text:
        return "disciplinary_rules"
    if "就業規則" in text:
        return "work_rules"
    if "給与" in text or "残業代" in text or "割増賃金" in text:
        return "payroll"
    if "採用" in text or "雇用" in text:
        return "hiring"
    return "labor_management"


