def render_generic_labor_article(
    title: str,
    brief: dict[str, Any],
    outline: list[dict[str, Any]],
    include_sources: bool,
) -> str:
    extraction = brief.get("extraction") if isinstance(brief.get("extraction"), dict) else {}
    keyword = str(extraction.get("target_seo_keyword") or "労務管理")
    reader = str(extraction.get("target_reader") or "中小企業の経営者、人事労務担当者")
    lines = [f"# {title}", ""]

    for section in outline:
        h2 = str(section["h2"])
        lines.extend([f"## {h2}", ""])
        lines.extend(
            [
                f"{h2}では、{reader}が{keyword}を実務に落とし込むために確認したい考え方を整理します。制度名や一般論だけでなく、自社の書類、説明、記録、管理職の判断基準までつなげて確認することが大切です。",
                "",
            ]
        )
        for h3 in section.get("h3", []):
            h3_text = str(h3)
            lines.extend([f"### {h3_text}", ""])
            lines.extend(
                [
                    f"{h3_text}は、{keyword}を運用するときに見落としやすい確認項目です。関係する規程、雇用契約書、説明資料、実施記録を一つずつ確認し、担当者だけの判断にしないことが重要です。",
                    f"社内で対応を進めるときは、対象者、判断基準、説明する内容、保存する記録を簡単な一覧にします。こうしておくと、従業員から質問を受けたときにも同じ基準で説明しやすくなります。",
                    "",
                ]
            )

    if include_sources:
        lines.extend(render_reference_section())

    return "\n".join(lines).rstrip() + "\n"


def render_reference_section(sources: list[dict[str, str]] | None = None) -> list[str]:
    sources = sources or LABOR_OFFICIAL_SOURCES
    lines = [
        "## 参考情報",
        "",
        "本文を実際に公開・提出する前には、以下の一次情報や最新の行政資料を確認してください。",
        "",
    ]
    for source in sources:
        lines.append(f"- {source['label']}: {source['url']}")
    lines.append("")
    return lines


def measure_article_quality(markdown: str) -> dict[str, int]:
    text = markdown_to_plain_text(markdown)
    compact = re.sub(r"\s+", "", text)
    return {
        "character_count": len(compact),
        "h2_count": len(re.findall(r"^##\s+", markdown, flags=re.MULTILINE)),
        "h3_count": len(re.findall(r"^###\s+", markdown, flags=re.MULTILINE)),
    }


def markdown_to_plain_text(markdown: str) -> str:
    text = re.sub(r"^#{1,6}\s*", "", markdown, flags=re.MULTILINE)
    text = re.sub(r"\[([^\]]+)\]\([^)]+\)", r"\1", text)
    text = re.sub(r"`([^`]+)`", r"\1", text)
    text = re.sub(r"\s+", " ", text)
    return text.strip()
