def clean_excerpt(value: str) -> str:
    text = " ".join(value.split())
    text = text.replace("訓 練", "訓練").replace("正社 員", "正社員").replace("社 員", "社員")
    text = text.replace("労働 者", "労働者").replace("中 小", "中小").replace("成⾧", "成長")
    text = text.replace("・OFF", "")
    return text.strip()
