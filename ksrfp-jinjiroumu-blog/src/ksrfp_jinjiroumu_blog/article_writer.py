def build_article_draft() -> dict[str, object]:
    rows = read_csv_dicts(TOPIC_SELECTION_DIR / "topic_selection_scores.csv")
    selected = select_current_or_recommended_topic(rows)
    if selected is None:
        result: dict[str, object] = {"status": "no_topic"}
        write_json(GENERATED_DIR / "articles" / "article_draft_latest.json", result)
        write_markdown(GENERATED_DIR / "articles" / "article_draft_latest.md", "# 記事本文ドラフト\n\n候補テーマがありません。")
        return result

    article = render_article(selected)
    result = {
        "status": "ok",
        "selected_topic": selected.get("topic_title", ""),
        "title": select_article_title(selected),
        "character_count": len(article),
        "path": str(GENERATED_DIR / "articles" / "article_draft_latest.md"),
    }
    write_json(GENERATED_DIR / "articles" / "article_draft_latest.json", result)
    write_markdown(GENERATED_DIR / "articles" / "article_draft_latest.md", article)
    return result


