def render_structured_article(
    title: str,
    intro: list[str],
    sections: list[tuple[str, list[tuple[str, list[str]]]]],
    summary: list[str],
) -> str:
    lines = [f"# {title}", "", "## はじめに", ""]
    for paragraph in intro:
        lines.extend([paragraph, ""])
    for heading, subsections in sections:
        lines.extend([f"## {heading}", ""])
        for subheading, paragraphs in subsections:
            lines.extend([f"### {subheading}", ""])
            padded_paragraphs = ensure_minimum_subsection_body(paragraphs, heading, subheading)
            for paragraph in padded_paragraphs:
                lines.extend([paragraph, ""])
    lines.extend(["## まとめ", ""])
    for paragraph in summary:
        lines.extend([paragraph, ""])
    return "\n".join(lines).rstrip() + "\n"


def ensure_minimum_subsection_body(paragraphs: list[str], heading: str, subheading: str) -> list[str]:
    compact_length = len("".join(paragraphs).replace(" ", "").replace("\n", ""))
    if compact_length >= 220:
        return paragraphs
    return paragraphs + [build_unique_support_paragraph(heading, subheading)]


def build_unique_support_paragraph(heading: str, subheading: str) -> str:
    variants = [
        "「{subheading}」は、{heading}を実行に移す前に社内で確認しておきたい論点です。関係する規程、契約書、対象者、説明資料、実施記録を一つずつ照合し、判断した日付と担当者を残しておくと、後日の問い合わせにも同じ基準で対応しやすくなります。",
        "{subheading}の確認では、{heading}に関係する書類と現場運用がずれていないかを見ることが大切です。担当者だけで抱えず、経営者、管理職、給与・勤怠担当者の役割を分け、必要な記録をどこへ保存するかまで決めておくと実務に落とし込みやすくなります。",
        "{heading}のなかでも{subheading}は、制度説明と記録管理が後回しになりやすい部分です。対象者、適用条件、確認資料、社内周知の方法を短いチェック項目にし、更新が必要な時期を管理表に入れておくと、属人的な判断を減らせます。",
        "社内で{subheading}を扱うときは、{heading}の目的に沿って、誰に何を説明し、どの資料で根拠を示すかを先に整理します。口頭だけで済ませると担当者ごとに対応が分かれやすいため、簡潔なメモや一覧表に残す運用が有効です。",
        "{subheading}については、制度の名称よりも実際の運用を確認する姿勢が重要です。対象者の範囲、判断基準、変更時の承認手順、従業員への伝え方を決めておけば、{heading}の見直しを一度きりの作業にせず継続管理につなげられます。",
    ]
    digest = hashlib.sha1(f"{heading}\n{subheading}".encode("utf-8")).hexdigest()
    template = variants[int(digest[:2], 16) % len(variants)]
    return template.format(heading=heading, subheading=subheading)


