def build_outline() -> dict[str, object]:
    rows = read_csv_dicts(TOPIC_SELECTION_DIR / "topic_selection_scores.csv")
    selected = select_current_or_recommended_topic(rows)
    if selected is None:
        result: dict[str, object] = {"status": "no_topic"}
        write_json(GENERATED_DIR / "outlines" / "article_outline_latest.json", result)
        write_markdown(GENERATED_DIR / "outlines" / "article_outline_latest.md", "# 記事構成案\n\n候補テーマがありません。")
        return result

    topic_type = classify_outline_topic(selected.get("topic_title", ""), selected.get("labels", ""))
    outline = select_outline_template(topic_type)
    result = {
        "status": "ok",
        "selected_topic": selected,
        "topic_type": topic_type,
        "outline_policy": {
            "h2_minimum": 7,
            "first_h2": "はじめに",
            "last_h2": "まとめ",
            "no_h1_in_body": True,
            "no_case_study_heading": True,
            "source_newsletter_terms_in_body": False,
        },
        "outline": outline,
    }
    write_json(GENERATED_DIR / "outlines" / "article_outline_latest.json", result)
    write_markdown(GENERATED_DIR / "outlines" / "article_outline_latest.md", render_outline(selected, outline, topic_type))
    return result


def classify_outline_topic(topic: str, labels: str) -> str:
    target = f"{topic} {labels}"
    if "トライアル雇用" in target:
        return "trial_employment_subsidy"
    if "子ども・子育て支援金" in target:
        return "childcare_support_contribution"
    if "出生時育児休業給付金" in target or "産後パパ育休" in target:
        return "childcare_leave_benefit"
    if "育介協定" in target or ("労使協定" in target and "管理職" in target):
        return "childcare_agreement"
    if "無期転換" in target:
        return "fixed_term_conversion"
    if "労働時間" in target or "働き方改革" in target or "36協定" in target:
        return "working_hours"
    if "パート" in target and ("有期" in target or "同一賃金" in target):
        return "equal_pay"
    if "助成金" in target or "subsidy" in labels:
        return "subsidy"
    if "カスハラ" in target or "ハラスメント" in target:
        return "harassment"
    if "ストレスチェック" in target or "メンタルヘルス" in target:
        return "mental_health"
    if "高年齢者" in target or "労働災害" in target or "安全衛生" in target:
        return "occupational_safety"
    if "社宅" in target or "現物給与" in target:
        return "payroll_housing"
    if "在職老齢年金" in target or "年金" in target:
        return "pension"
    if "障害者雇用" in target:
        return "disability_employment"
    if "育児" in target or "介護" in target or "柔軟な働き方" in target:
        return "childcare_care"
    return "labor_management"


