def render_outline(selected: dict[str, str], outline: list[dict[str, object]], topic_type: str) -> str:
    generated_at = datetime.now().strftime("%Y-%m-%d %H:%M")
    lines = [
        "# 記事構成案",
        "",
        f"生成日時: {generated_at}",
        "",
        f"対象テーマ: {selected.get('topic_title', '')}",
        f"テーマ種別: {topic_type}",
        f"出典PDF: {selected.get('pdf_name', '')}",
        "",
        "## 構成ルール",
        "",
        "- H2見出しは7個以上",
        "- 最初のH2は「はじめに」",
        "- 最後のH2は「まとめ」",
        "- 本文用H1は使わない",
        "- 「事例」「成功例」を見出しに入れない",
        "- PDF名や「人事労務だより」という出典管理語を本文に出さない",
        "",
        "```markdown",
    ]
    for block in outline:
        lines.append(f"## {block['h2']}")
        note = block.get("note")
        if note:
            lines.append(str(note))
            lines.append("")
        for heading in block.get("h3", []):
            lines.append(f"### {heading}")
        lines.append("")
    lines.append("```")
    return "\n".join(lines)
